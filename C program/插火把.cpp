#include<bits/stdc++.h>
using namespace std;
int main()
{
	int a[100][100]={0},i,j,k,m,n,x,y;
	cin>>n>>m>>k;
	for(i=2;i<=m+1;i++)
	{
		cin>>x>>y;
		x=x-1,y=y-1;
		a[y][x-2]=1;
		a[y][x-1]=1;
		a[y][x]=1;
		a[y][x+1]=1;
		a[y][x+2]=1;
		a[y-2][x]=1;
		a[y-1][x-1]=1;
		a[y-1][x]=1;
		a[y-1][x+1]=1;
		a[y+1][x-1]=1;
		a[y+1][x]=1;
		a[y+1][x+1]=1;
		a[y+2][x]=1;
	}
	for(;i<=m+k+1;i++)
	{
		cin>>x>>y;
		x=x-1,y=y-1;
		a[y-2][x-2]=1;
		a[y-2][x-1]=1;
		a[y-2][x]=1;
		a[y-2][x+1]=1;
		a[y-2][x+2]=1;
		a[y-1][x-2]=1;
		a[y-1][x-1]=1;
		a[y-1][x]=1;
		a[y-1][x+1]=1;
		a[y-1][x+2]=1;
		a[y][x-2]=1;
		a[y][x-1]=1;
		a[y][x]=1;
		a[y][x+1]=1;
		a[y][x+2]=1;
		a[y+1][x-2]=1;
		a[y+1][x-1]=1;
		a[y+1][x]=1;
		a[y+1][x+1]=1;
		a[y+1][x+2]=1;
		a[y+2][x-2]=1;
		a[y+2][x-1]=1;
		a[y+2][x]=1;
		a[y+2][x+1]=1;
		a[y+2][x+2]=1;
	}
	k=0;
	for(i=0;i<n;i++)
	{
		for(j=0;j<n;j++)
		if(a[i][j]==1)
		k++;
	}
	cout<<n*n-k;
	return 0;
}